#ifndef LLVM_LIB_TARGET_TBD_INSTPRINTER_TBDINSTPRINTER_H
#define LLVM_LIB_TARGET_TBD_INSTPRINTER_TBDINSTPRINTER_H

#include "llvm/MC/MCInstPrinter.h"

namespace llvm {
  class TBDInstPrinter : public MCInstPrinter {
  public:
    TBDInstPrinter(const MCAsmInfo &MAI, const MCInstrInfo &MII,
                      const MCRegisterInfo &MRI)
      : MCInstPrinter(MAI, MII, MRI) {}

    void printInst(const MCInst *MI, raw_ostream &O, StringRef Annot,
                   const MCSubtargetInfo &STI) override;

    // Autogenerated by tblgen. We don't have to change it 
    void 		printInstruction(const MCInst *MI, raw_ostream &O);
    static const char *	getRegisterName(unsigned RegNo);
	
    // We have to implement following method
    void printOperand(const MCInst *MI, unsigned OpNo, raw_ostream &O, const char *Modifier = nullptr);
  };
}

#endif
